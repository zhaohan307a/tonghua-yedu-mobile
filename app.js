function pinyinKey(text) {
  let hash = 2166136261;
  for (let index = 0; index < text.length; index += 1) {
    hash ^= text.charCodeAt(index);
    hash = Math.imul(hash, 16777619);
  }
  return (hash >>> 0).toString(36);
}

function escapeHtml(text) {
  return text
    .replace(/&/g, "&amp;")
    .replace(/</g, "&lt;")
    .replace(/>/g, "&gt;")
    .replace(/"/g, "&quot;")
    .replace(/'/g, "&#39;");
}

function renderRubyText(text) {
  const pinyin = PINYIN_LINES[pinyinKey(text)];
  if (!pinyin) {
    return escapeHtml(text);
  }

  return Array.from(text)
    .map((char, index) => {
      const sound = pinyin[index];
      if (!sound || sound === char || !/[\u3400-\u9fff]/.test(char)) {
        return escapeHtml(char);
      }
      return `<ruby>${escapeHtml(char)}<rt>${escapeHtml(sound)}</rt></ruby>`;
    })
    .join("");
}

function mobileImage(src) {
  return src.replace(/\.jpg$/, "-mobile.jpg");
}

function webpImage(src) {
  return src.replace(/\.jpg$/, ".webp");
}

function imageSourceSet(src, extension) {
  const base = extension === "webp" ? webpImage(src) : src;
  const mobile = extension === "webp" ? webpImage(mobileImage(src)) : mobileImage(src);
  return `${mobile} 800w, ${base} 1200w`;
}

function imageMarkup(src, alt, loading = "lazy") {
  const sizes = "(max-width: 760px) calc(100vw - 28px), 840px";
  return `<picture>
    <source type="image/webp" srcset="${imageSourceSet(src, "webp")}" sizes="${sizes}" />
    <img src="${src}" srcset="${imageSourceSet(src, "jpg")}" sizes="${sizes}" alt="${escapeHtml(alt)}" loading="${loading}" decoding="async" />
  </picture>`;
}

